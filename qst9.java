/* Write a program to Swap the Variable */

public class qst9 {
    public static void main(String[] args) {
        System.out.println("NO Before Swap:");
        int a=5;
        int b=6;
        int c=a+b;
        System.out.println("a: "+a);
        System.out.println("b: "+b);
        a= c-a;
        b= c-b;
        System.out.println("No After Swapping:");
        System.out.println("a: "+a);
        System.out.println("b: "+b);

    }
}
